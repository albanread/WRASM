# gen_stdverbs.ps1 — data-driven generator for the nano-TCL standard verb table.
#
# Scans the standard GamesCanvas library modules for public (capital-initial)
# `proc`s and emits library/tcl/stdverbs.was, which registers each as a nano-TCL
# verb (verb name == proc name) via TclAddVerb. Re-run whenever the library API
# changes — no hand-maintenance.
#
#   pwsh library/tcl/gen_stdverbs.ps1
#
# Only modules a canvas game actually links are scanned (so every referenced
# proc resolves at assemble time). Optional modules (fx/tile/audio) a game can
# register itself, or add them to $modules here when they are linked.

$lib     = Split-Path (Split-Path $PSCommandPath)        # ...\library
$modules = @("canvas", "blit", "sprite", "input", "introspect")
# internal-only procs the harness drives — not useful as script verbs
$skip    = @("InputKey", "InputMouse", "InputPoll")

$verbs = [System.Collections.Generic.List[string]]::new()
foreach ($m in $modules) {
    $path = Join-Path $lib "$m.was"
    Get-Content $path | ForEach-Object {
        if ($_ -cmatch '^proc\s+([A-Z][A-Za-z0-9_]*)') {   # -cmatch: case-sensitive, capital-initial only
            $name = $Matches[1]
            if ($skip -notcontains $name -and -not $verbs.Contains($name)) { $verbs.Add($name) }
        }
    }
}

$nl = "`r`n"
$o  = [System.Collections.Generic.List[string]]::new()
$o.Add("; stdverbs.was - GENERATED by gen_stdverbs.ps1. Do not edit by hand.")
$o.Add("; Registers the standard GamesCanvas library procs as nano-TCL verbs.")
$o.Add("; Modules scanned: $($modules -join ', ').  $($verbs.Count) verbs.")
$o.Add("; Include AFTER the library + library/tcl/tcl.was; call TclStdVerbs once.")
$o.Add("")
$o.Add("module Tcl")
$o.Add("")
$o.Add(".DATA")
$o.Add(".balign 8")
foreach ($v in $verbs) { $o.Add("svn_$v BYTE `"$v`", 0") }
$o.Add(".balign 16")
$o.Add("")
$o.Add(".CODE")
$o.Add("")
$o.Add("; ---- TclStdVerbs: register every standard library proc as a verb ----")
$o.Add("proc TclStdVerbs frame")
foreach ($v in $verbs) {
    $o.Add("    lea     rcx, [rip + svn_$v]")
    $o.Add("    lea     rdx, [rip + $v]")
    $o.Add("    call    TclAddVerb")
}
$o.Add("endproc")
$o.Add("")
$o.Add("endmodule")

$out = Join-Path $lib "tcl\stdverbs.was"
($o -join $nl) | Set-Content -Encoding ASCII $out
"generated $($verbs.Count) verbs -> $out"
"verbs: $($verbs -join ' ')"
