# gen_stdverbs.ps1 — data-driven generator for the nano-TCL standard verb tables.
#
# Scans each backend's library modules for public CALLABLE procs and emits a
# per-backend registration fragment (a `TclStdVerbs` proc that registers each
# as a nano-TCL verb, verb name == proc name):
#
#   library/tcl/stdverbs_cpu.was   <- library/  (the software backend)
#   library/tcl/stdverbs_gpu.was   <- gpu/      (the D3D11 backend)
#
# stdverbs.was is a hand-written shell that includes the right one via
# `IFDEF NANOTCL_GPU`. Re-run this when either backend's API changes:
#
#   powershell library/tcl/gen_stdverbs.ps1
#
# Two export styles are recognised (the GPU backend mixes both):
#   * `proc Name ... endproc`             — the clean macro form (CPU + gpu canvas/input)
#   * `.globl Name` backed by a `Name:` label — bare-label procs (gpu blit/sprite/text/
#     introspect, and a few canvas entries: Text/FillCircle). Data exports
#     (`.globl Name` with no `Name:` label, e.g. CanvasW) are NOT labels -> skipped,
#     so they never become a verb that would jump into data.
# $skip drops setup/lifecycle procs that are public but not scripting verbs.

$root = Split-Path (Split-Path $PSCommandPath)            # ...\library
$repo = Split-Path $root                                 # ...\RASM
$skip = @(
    # input internals (the harness drives these; KeyDown/Action/... are the verbs)
    "InputKey", "InputMouse", "InputPoll",
    # GPU lifecycle / per-frame internals — callable, but not ad-hoc scripting verbs
    "GpuInit", "GpuPresent", "GpuSnapshot", "GpuComposite",
    "TxtInit", "TxtSetColour", "TxtSetXform", "TxtClear", "TxtUpload", "TxtFlush",
    "SprInit", "SprAddFrame", "SprUploadAtlas", "SprSetPalette", "SprUploadPalette",
    "SprClear", "SprPush", "SprUpload",
    "BlitInit", "PoolAlloc", "BlitSetKey"
)

function Gen([string]$dir, [string[]]$modules, [string]$out, [string]$label) {
    $verbs = [System.Collections.Generic.List[string]]::new()
    foreach ($m in $modules) {
        $path = Join-Path $dir "$m.was"
        if (-not (Test-Path $path)) { continue }
        $lines = Get-Content $path
        # capital-initial code labels in this file: `proc Name` and bare `Name:`
        $procs  = @{}
        $labels = @{}
        $globls = @{}
        foreach ($ln in $lines) {
            if ($ln -cmatch '^proc\s+([A-Z][A-Za-z0-9_]*)')   { $procs[$Matches[1]]  = $true }
            if ($ln -cmatch '^([A-Z][A-Za-z0-9_]*):')          { $labels[$Matches[1]] = $true }
            if ($ln -cmatch '^\.globl\s+([A-Z][A-Za-z0-9_]*)') { $globls[$Matches[1]] = $true }
        }
        # a verb is a proc-form proc, OR a .globl'd name that is a real code label
        $names = [System.Collections.Generic.List[string]]::new()
        foreach ($p in $procs.Keys)  { [void]$names.Add($p) }
        foreach ($g in $globls.Keys) { if ($labels.ContainsKey($g)) { [void]$names.Add($g) } }
        foreach ($n in ($names | Sort-Object)) {
            if ($skip -notcontains $n -and -not $verbs.Contains($n)) { $verbs.Add($n) }
        }
    }
    $nl = "`r`n"
    $o = [System.Collections.Generic.List[string]]::new()
    $o.Add("; $(Split-Path $out -Leaf) - GENERATED by gen_stdverbs.ps1. Do not edit by hand.")
    $o.Add("; $label backend: $($modules -join ', ') public procs as nano-TCL verbs.")
    $o.Add("; $($verbs.Count) verbs. Selected by stdverbs.was via IFDEF NANOTCL_GPU.")
    $o.Add("")
    $o.Add("module Tcl")
    $o.Add("")
    $o.Add(".DATA")
    $o.Add(".balign 8")
    foreach ($v in $verbs) { $o.Add("svn_$v BYTE `"$v`", 0") }
    $o.Add(".balign 16")
    $o.Add("")
    $o.Add(".CODE")
    $o.Add("")
    $o.Add("; ---- TclStdVerbs: register every $label library proc as a verb ----")
    $o.Add("proc TclStdVerbs frame")
    foreach ($v in $verbs) {
        $o.Add("    lea     rcx, [rip + svn_$v]")
        $o.Add("    lea     rdx, [rip + $v]")
        $o.Add("    call    TclAddVerb")
    }
    $o.Add("endproc")
    $o.Add("")
    $o.Add("endmodule")
    ($o -join $nl) | Set-Content -Encoding ASCII (Join-Path $root "tcl\$out")
    "  $out : $($verbs.Count) verbs ($label) -> $($verbs -join ' ')"
}

"generating nano-TCL std verb tables:"
Gen (Join-Path $repo "library") @("canvas","blit","sprite","input","introspect")       "stdverbs_cpu.was" "CPU"
Gen (Join-Path $repo "gpu")     @("canvas","blit","sprite","input","introspect","text") "stdverbs_gpu.was" "GPU"
